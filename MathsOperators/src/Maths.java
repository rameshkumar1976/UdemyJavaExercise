public class Maths {

    public static void main(String args[]){

        Integer a,b,c;
        a=10;b=15;
        c=a+b;
        System.out.println("The value of a + b is :"+c);

        a=25;b=8;
        c=a-b;
        System.out.println("The value of a - b is :"+c);

        c=a*b;
        System.out.println("The value of a * b is :"+c);

        a=100;b=5;
        c=a/b;
        System.out.println("The value of a / b is :"+c);
    }
}
